#include "libft.h"

void	ft_free_table(char ***tab)
{
	size_t	i;

	i = 0;
	while (tab[0][i])
	{
		free(tab[0][i]);
		i++;
	}
	free(tab[0]);
	tab[0] = (char **)0;
}
